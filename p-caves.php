<p style="margin-left:10px;font-size: 35px;color:#000;">Buddhist Caves of Khapra Kodiya<p>
<img src="img/Buddhist-Caves-of-Khapra-Kodiya-Thumbnail.jpeg" class="img-fluid" alt="...">

<p style="margin-left:10px;font-size:20px; padding-top:10px;">
<b style="color:#000;">About The Location :</b>The so-called “Buddhist Caves” around Uparkot are not actually caves, but three separate sites of rooms carved out of stone to be used as monks’ quarters, hence the name. They are all a little over 2000 years old, give or take a couple of centuries.
</p>

<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    The oldest, the Khapara Kodia caves belong to 3rd-4th century AD and are the plainest of all cave groups. These caves are along the edge of the ancient Sudarshan Lake (which no longer exists) and the northern side of Uparkot. The chambers are separated into an east-west longitudinal crest. The rectangle western wing and the ‘L’ shaped wing used by the Monks as a monsoon shelter, are the important parts of the caves. They were carved into living rock during the reign of Emperor Ashoka and are considered the earliest monastic settlement in the area. After many years of use, they were abandoned because cracks above them allowed water to seep into living quarters, rendering them unusable. Many accounts say that after this, the monks left for Maharashtra, where they went on to carve many similar and more elaborate structures. Khapara Kodia was damaged by later quarrying, and now only the highest story remains.
</p><br>
<p style="margin-left:10px;font-size: 35px;color:#000;">Baba Pyare Caves<p>
<img src="img/Baba-Pyare-Caves-Thumbnail.jpeg" class="img-fluid" alt="...">
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    <b style="color:#000;">About The Location:</b> Baba Pyara caves are lying close to the Modhimath, which has four caves in its northern group. The next set of south group caves has a unified plan with a spacious court and a chaitya hall. The art tradition of the Satavahanas period has influence over these cave pillars and door jambs of the caves. It is believed that they belong to the 1st – 2nd century AD. These have 13 rooms in three stories, cut into the rock 45 m. (150 ft.) high and adorned with carvings of Buddhist symbology. These are much more intact than the Khapara Kodia caves. The last (and most recent, being only 1900 years old) caves are next to the Adi-Kadi Vav.
</p><br>

<p style="margin-left:10px;font-size: 35px;color:#000;">Khambhalida Buddhist Caves<p>
<img src="img/Khambalida-Caves-Thumbnail.jpeg" class="img-fluid" alt="...">
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    <b style="color:#000;">About the location:</b> Khambalida is where Buddhism made a deep impact around the 4th century. Remnants of this can be seen in the clutch of 15 caves that have a chaitya-griha or a shrine in the centre. Intricate sculptures of Boddhisatvas, Avalokiteshvara and Vajrapani flank the entrance of these limestone caves.
</p>
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    <b style="color:#000;">Brief History:</b> The history of these caves dates back to the 4th century when Buddhism was gaining momentum in this part of the country. They were discovered in 1958 by renowned archaeologist, PP Pandya
</p>
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    <b style="color:#000;">Best time to visit:</b> November–February.
</p><br>

<p style="margin-left:10px;font-size: 35px;color:#000;">Taranga Caves<p>
<img src="img/Taranga-caves-Banner.jpeg" class="img-fluid" alt="...">
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    <b style="color:#000;">About the location:</b> Northeast of the main gate of the fort is a cave where the air is extraordinarily cool. The cave, locally known as Jogida ni Gufa, was found and used by Buddhist monks thousands of years ago. It has Buddhist sculptures carved in stone that resemble the Bodhi Vriksha or Kalpavruksha and other Buddhist motifs. You also come across some beautiful Buddhist sculptures on the mountain on the way to the cave. Taranga Hills, 20 km from Vadnagar, on the Aravali range, harbours profound connections with Buddhism. The main idols enshrined in the Taranmata and Dharanmata temples are of Buddhist goddess Tara. Broken terracotta images of Buddha, four carved images of Dhyani Buddha on a stone plate, stone and brick walls inside rock shelters, etc. have been discovered from here.
</p>
