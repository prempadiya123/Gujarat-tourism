<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    <b style="color:#000;">About the location:</b> Creating history on an already hallowed address of an erstwhile palace (Moti Shahi Mahal), this is one of the most underrated experiences of Ahmedabad. The museum is a rich repository of Patel’s life arc as a political leader and thinker, while giving considerable space to other luminaries who crossed his path – namely Rabindranath Tagore, who stayed here at the age of seventeen. Contemporary technology backs the interactive exhibits with clear, concise and well researched audio-visual displays.
</p>
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
The museum stands on the same address as Moti Shahi Mahal, a palace built by the Mughal Emperor Shahjahan between 1618 and 1622. One of the biggest inclusions for ornamentation included the Shahibaug gardens that were brimming with noble cypresses, cedars, palms, sandals, and cassias, with mango, tamarind, and other fruit trees.
</p>
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
The palace fell into the hands of the British and was used as a government building. Post-independence, the structure became Raj Bhavan, the official residence of the Governor of Gujarat, from 1960 to 1978. It was declared a memorial for Sardar Vallabhbhai Patel on his centennial birth anniversary on 7 March 1980.
