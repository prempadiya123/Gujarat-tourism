<p style="margin-left:10px;font-size: 35px;color:#000;">Rani Ki Vav<p>
<img src="img/Rani-Ki-Vav-Thumbnail.jpeg" class="img-fluid" alt="...">

<p style="margin-left:10px;font-size:20px; padding-top:10px;">
<b style="color:#000;">About The Location :</b>The only real sign of Patan’s former glory is this astoundingly beautiful step-well. Located on the banks of the Saraswati River, this is one of the oldest and finest stepwells in Gujarat and is remarkably preserved. Steps lead down through multiple levels with lines of carved pillars and more than 800 sculptures, mostly on Vishnu-avatar themes, as well as striking geometric patterns. The stepwell is made in the form of an inverted temple. It showcases superior craftsmanship of the era, and the unique Maru-Gurjara architectural style and houses sculptures similar to the Vimalavasahi temple in Mount Abu and Sun temple at Modhera.
</p>
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
<b style="color:#000;">Brief History :</b>The stepwell was built in 1063 by Rani Udayamati of the Chaulukya Dynasty to commemorate her husband, Bhimdev I. A 1304 composition of Jain monk, Merutunga, mentions that Udayamati, the daughter of Naravaraha Khangara, built this stepwell at Patan.
</p><br>
