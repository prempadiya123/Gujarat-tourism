<p style="margin-left:10px;font-size: 35px;color:#000;">Tana-Riri Mhotsav<p>
<img src="img/tana-riri-mhotsav.jpeg" class="img-fluid" alt="...">
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    <b style="color:#000;">About The Location:</b> The state government organizes the Tana Riri Festival in memory of Tana and Riri, two sisters of Vadnagar. Both the sisters were famous among locals for their powerful singing abilities and their biographies are part of Gujarati folklore.
</p>
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    During Akbar's reign in India, Tansen, his court musician, once sang a raga deepak that was said to invoke fire and heat, which caused his health to deteriorate. With no cure other than counter musical therapy, the sister duo sang Raag Malhar for him, which effectively cured Tansen.
</p>
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    The Tana Riri Festival is dedicated to the rich music of India and musicians from across the country join the festival to showcase the beauty of Indian music in various forms. Musicians are also honored with the Tana Riri Award for their talent.
</p>
<p style="margin-left:10px;font-size:20px; padding-top:10px;">
    The festival was started by the then Chief Minister Shri Narendra Modi in 2010 and a memorial has been built at Vadnagar in memory of the sisters. In 2021, more than 110 artists set a world record by playing the traditional instrument Bhungal for five minutes simultaneously during the festival. The actors belonged to Turi Barot and Nayak Samaj of the state.
</p><br>
